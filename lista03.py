#Imprimindo elementos das listas:

x = [['Lanes', 33], ['Jhosef', 13], ['Tio', 40]]
#Primeira lista é a do Lanes a segunda lista é a do Jhosef e a terceira lista do Tio
# Todas essas 3 listas acima estão dentro de uma lista só.

print(x[0][1])# Neste caso está imprimindo da primeira lista o valor 33
              # Na primeira lista o valor 33 está no índice 1 da lista e o nome Lanes no índice 0.

print(x[1][0])# Neste caso está imprimindo da segundo lista o nome Jhosef
              # Na segunda lista o nome Jhosef está no índice 0 e o 13 no índice 1.

print(x[2][0])# Neste caso está imprimendo da terceira lista o nome Tio
              # Na terceira lista o nome Tio está no ídice 0 e o 40 está no índice 1.